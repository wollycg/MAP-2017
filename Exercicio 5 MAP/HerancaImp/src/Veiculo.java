
public class Veiculo 
{	String modelo;
	int numLugares;
	int comprimento;
	int ano;
	String cor;
	
	public Veiculo(String modelo, int numLugares, int comprimento, int ano, String cor) 
	{
		
		this.modelo = modelo;
		this.numLugares = numLugares;
		this.comprimento = comprimento;
		this.ano = ano;
		this.cor = cor;
	}
	

}
